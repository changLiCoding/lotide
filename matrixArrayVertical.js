const matrixArrVertical = (arr) => {
  const results = [];
  for (let row = 0; row < arr[row].length; row++) {
    const colArr = [];
    for (let col = 0; col < arr.length; col++) {
      colArr.push(arr[col][row]);
    }
    results.push(colArr);
  }
  return results;
};

